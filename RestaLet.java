public abstract class RestaLet
{
  public static char restaLet (char getPoscX)
   {
    if (getPoscX=='B')
    return 'A';
    else
    if (getPoscX=='C')
       return 'B';
    else
     if (getPoscX=='D')
        return 'C';
    else
     if (getPoscX=='E')
      return 'D';
    else
     if (getPoscX=='F')
     return 'E';
     else
     if (getPoscX=='G')
     return 'F';
     else
     if (getPoscX=='H')
     return 'G';
     else
     return 0;
   }

}