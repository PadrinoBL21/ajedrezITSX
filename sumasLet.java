public abstract class sumasLet
{
   public static char sumLet (char getPosicionX)
   {
    if (getPosicionX=='A')
    return 'B';
    else
    if (getPosicionX=='B')
     return 'C';
     else
     if (getPosicionX=='C')
     return 'D';
     else
     if (getPosicionX=='D')
     return 'E';
     else
     if (getPosicionX=='E')
     return 'F';
     else
     if (getPosicionX=='F')
     return 'G';
     else
     if (getPosicionX=='G')
     return 'H';
     else
     return 0;
   }

}